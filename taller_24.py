def lagrange_interpolation(points):
    def L(i, x, points):
        result = 1
        for j, point in enumerate(points):
            if i != j:
                result *= (x - point[0]) / (points[i][0] - point[0])
        return result

    def P(x, points):
        result = 0
        for i, point in enumerate(points):
            result += point[1] * L(i, x, points)
        return result

    return P

points = [(1, 2), (2, 1.8), (3, -2), (4, -4.6), (5, 3.6)]

interpolating_polynomial = lagrange_interpolation(points)

